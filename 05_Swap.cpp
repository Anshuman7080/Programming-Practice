#include <bits/stdc++.h>
using namespace std;
 
int solution()
{
  
    //Two integers to perform the swapping operation 
    int x, y;
  
    //Taking input for two integers
    cin >> x >> y;
  
    //Using function to swap the two integers as asked in the question
    swap(x, y);
  
    //Printing the swapped values
    cout << x <<" "<< y;
    return 0;
}

int main()
{
  
    //Function call
    solution();
    return 0;
}
